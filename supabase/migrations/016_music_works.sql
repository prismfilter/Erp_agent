CREATE TABLE IF NOT EXISTS public.music_works (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  no INT NOT NULL UNIQUE,            -- 전역 순번, 중복 입력 불가
  writer_name TEXT NOT NULL,         -- 작가명
  komca_code TEXT,                   -- KOMCA 저작물 코드(식별자 → TEXT)
  song_title TEXT NOT NULL,          -- 곡명
  artist TEXT,                       -- 아티스트
  domestic_share NUMERIC,            -- 국내 지분(%)
  overseas_share NUMERIC,            -- 국외 지분(%)
  rate NUMERIC,                      -- 요율
  recontract_date DATE,              -- 전속작가 재계약일
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_music_works_writer ON public.music_works(writer_name, no);

ALTER TABLE public.music_works ENABLE ROW LEVEL SECURITY;

-- ADMIN/STAFF만 전 작업 가능 (WRITER 차단)
DROP POLICY IF EXISTS staff_all_music_works ON public.music_works;
CREATE POLICY staff_all_music_works ON public.music_works FOR ALL
  USING     (EXISTS (SELECT 1 FROM public.user_roles WHERE user_id = auth.uid() AND role IN ('ADMIN','STAFF')))
  WITH CHECK(EXISTS (SELECT 1 FROM public.user_roles WHERE user_id = auth.uid() AND role IN ('ADMIN','STAFF')));


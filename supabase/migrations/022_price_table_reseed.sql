-- ── 자체 점검: 카테고리별 행 수가 권위 원본과 일치하는지 확인 ──────────────
-- 기대값: 앨범 22 / 방송·공연·시상식 21 / 광고 5 / 기타 7 / 밴드(플레디스) 16 / 밴드 18 = 89.
DO $$
DECLARE
  total INT;
BEGIN
  SELECT count(*) INTO total FROM public.price_items;
  IF total <> 89 THEN
    RAISE EXCEPTION '재시드 행 수 불일치: 기대 89, 실제 %', total;
  END IF;
  PERFORM 1 FROM (
    SELECT category, count(*) AS c FROM public.price_items GROUP BY category
  ) g
  WHERE NOT (
    (g.category = '앨범' AND g.c = 22) OR
    (g.category = '방송·공연·시상식' AND g.c = 21) OR
    (g.category = '광고' AND g.c = 5) OR
    (g.category = '기타' AND g.c = 7) OR
    (g.category = '밴드(플레디스)' AND g.c = 16) OR
    (g.category = '밴드' AND g.c = 18)
  );
  IF FOUND THEN
    RAISE EXCEPTION '카테고리별 행 수 불일치';
  END IF;
END $$;
